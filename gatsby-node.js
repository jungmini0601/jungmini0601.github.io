const path = require('path')

// 절대 경로 매핑을 줄이기 위한 웹팩 설정 공식 문서 참조
exports.onCreateWebpackConfig = ({ stage, actions }) => {
  actions.setWebpackConfig({
    resolve: {
      modules: [path.resolve(__dirname, "src"), "node_modules"],
    },
  })
}
